package productlist;

public class ProductListBean {
	int plnum;
	String plname;
	String plcategory;
	String plcompany;
	int plprice;
	public int getPlnum() {
		return plnum;
	}
	public void setPlnum(int plnum) {
		this.plnum = plnum;
	}
	public String getPlname() {
		return plname;
	}
	public void setPlname(String plname) {
		this.plname = plname;
	}
	public String getPlcategory() {
		return plcategory;
	}
	public void setPlcategory(String plcategory) {
		this.plcategory = plcategory;
	}
	public String getPlcompany() {
		return plcompany;
	}
	public void setPlcompany(String plcompany) {
		this.plcompany = plcompany;
	}
	public int getPlprice() {
		return plprice;
	}
	public void setPlprice(int plprice) {
		this.plprice = plprice;
	}
}
